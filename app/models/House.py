class House():
    
     def __init__(self, id, photo, city, state, zip_code, 
     price, rooms, bathrooms, longitude, latitude, description,
     status, type):
        self.id = id
        self.photo = photo
        self.city = city
        self.state = state
        self.zip_code = zip_code
        self.price = price
        self.rooms = rooms
        self.bathrooms = bathrooms
        self.longitude = longitude
        self.latitude = latitude
        self.description = description
        self.status = status
        self.type = type